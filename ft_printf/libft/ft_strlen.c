#include "libft.h"

size_t	ft_strlen(const char *s)
{
	char	*ptr;
	size_t	cnt;

	cnt = 0;
	ptr = (char*)(s);
	while (ptr[cnt])
	{
		cnt++;
	}
	return (cnt);
}
